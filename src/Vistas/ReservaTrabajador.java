    public void agregarReservaAlTabla(Reserva reserva) {
    // Crear una fila con los datos de la reserva
    Object[] fila = new Object[9];
    fila[0] = reserva.getDni();
    fila[1] = reserva.getNombre();
    fila[2] = reserva.getApellido();
    fila[3] = reserva.getCelular();
    fila[4] = reserva.getFecha();
    fila[5] = reserva.getServicio();
    fila[6] = reserva.getHora();
    fila[7] = reserva.getJornada();
    fila[8] = reserva.getCorreo();
        model.addRow(fila);
}


    
